    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        
        try {
            Connection con = Principal.ConexaoMySQL.getInstance().getConnection();
            
            String cmd = "SELECT idcurso FROM cursos WHERE nomeCurso = '"+cbxCurso.getModel().getSelectedItem()+"'";
            
            ResultSet res = con.createStatement().executeQuery(cmd);
            res.next();
            
            this.idCursoDB = res.getInt("idcurso");
            
            res.previous();
            
            cmd = "INSERT INTO disciplinas (nomeDisciplina, cargaHoraria, numeroVagas, periodo, cursos_idCurso)"
                    + "VALUES ('"+txtNomeDisciplina.getText()+"',"+txtCargaHoraria.getText()+","+txtNumVagas.getText()
                    +",'"+this.periodo+"',"+this.idCursoDB+")";
            
            System.out.printf(cmd);
            
            con.createStatement().executeUpdate(cmd);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

