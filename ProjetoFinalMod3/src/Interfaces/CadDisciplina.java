    private void carregarCombo()
    {
        try {
            Connection con = Principal.ConexaoMySQL.getInstance().getConnection();
            
            String cmd = "SELECT nomeCurso FROM cursos";
            
            ResultSet resC = con.createStatement().executeQuery(cmd);
            while(resC.next())
            {
                String nomeCurso = resC.getString("nomeCurso");
                ((DefaultComboBoxModel) cbxCurso.getModel()).addElement(nomeCurso);
            }
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }

